#include <iostream>

using namespace std;

int main() {
	
	int x, y, z;

	cout << "First Number: ";
	cin >> x;
	cout << "Second Number: ";
	cin >> y;
	cout << "Third Number: ";
	cin >> z;

	cout << "\nSum: " << x << " + " << y << " + " << y << " = " << x + y + z;
	cout << "\nProduct: " << x << " * " << y << " * " << y << " = " << x * y * z << "\n\n";
}